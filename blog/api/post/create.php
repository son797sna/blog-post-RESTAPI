  include_once '../../config/Database.php';
  include_once '../../models/Post.php';

  // Instantiating DB class
  $database = new Database();
  $post = new Post();

  // Getting the data
  $data = json_decode(file_get_contents("php://input"));

  // Create post
  if($post->create($data)) {
    echo json_encode(
      array('message' => 'Post Created')
